package examples.rockets;

public class Boundary {

	float downBoundary, leftBoundary, rightBoundary, topBoundary;

	public Boundary(float downBoundary, float leftBoundary, float rightBoundary, float topBoundary) {
		super();
		this.downBoundary = downBoundary;
		this.leftBoundary = leftBoundary;
		this.rightBoundary = rightBoundary;
		this.topBoundary = topBoundary;
	}
	
}
